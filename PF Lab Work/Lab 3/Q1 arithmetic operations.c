#include <stdio.h>
int main() 
{
    int a = 10, b = 5;
    
    printf("Sum: %d + %d = %d\n", a, b, a + b);
    printf("Difference: %d - %d = %d\n", a, b, a - b);
    printf("Product: %d * %d = %d\n", a, b, a * b);
    printf("Quotient: %d / %d = %d\n", a, b, a / b);
    printf("Remainder: %d %% %d = %d\n", a, b, a % b);

    return 0;
}

